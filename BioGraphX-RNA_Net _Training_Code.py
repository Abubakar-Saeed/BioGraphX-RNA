# 4. EVALUATION METRICS
# ==========================================
def evaluate_metrics(model, dataloader, threshold=0.5):
    """
    Calculate metrics strictly on Benchmark Classes following " " protocol.
    """
    model.eval()
    all_targets, all_logits = [], []
    
    with torch.no_grad():
        for emb, phys, labels in dataloader:
            emb, phys = emb.to(DEVICE), phys.to(DEVICE)
            logits = model(emb, phys)
            probs = torch.sigmoid(logits)
            
            # Handle any NaN values
            if torch.isnan(probs).any():
                probs = torch.nan_to_num(probs, nan=0.0)
                
            all_logits.append(probs.cpu().numpy())
            all_targets.append(labels.numpy())
            
    y_probs = np.vstack(all_logits)
    y_true = np.vstack(all_targets)
    y_pred = (y_probs > threshold).astype(int)
    
    # Initialize metric containers
    metrics = {"auroc": [], "auprc": [], "f1": [], "mcc": [], "prec": [], "rec": []}
    
    # Calculate per-class metrics
    for i in range(NUM_CLASSES):
        # AUROC and AUPRC (handle constant columns)
        try:
            if len(np.unique(y_true[:, i])) > 1:
                auroc = roc_auc_score(y_true[:, i], y_probs[:, i])
                auprc = average_precision_score(y_true[:, i], y_probs[:, i])
            else:
                auroc, auprc = 0.5, 0.0
        except:
            auroc, auprc = 0.5, 0.0
            
        # Classification metrics
        f1 = f1_score(y_true[:, i], y_pred[:, i], zero_division=0)
        mcc = matthews_corrcoef(y_true[:, i], y_pred[:, i])
        prec = precision_score(y_true[:, i], y_pred[:, i], zero_division=0)
        rec = recall_score(y_true[:, i], y_pred[:, i], zero_division=0)
        
        metrics["auroc"].append(auroc)
        metrics["auprc"].append(auprc)
        metrics["f1"].append(f1)
        metrics["mcc"].append(mcc)
        metrics["prec"].append(prec)
        metrics["rec"].append(rec)
    
    # Macro Average on BENCHMARK CLASSES ONLY (" " protocol)
    benchmark_aurocs = [metrics["auroc"][i] for i in BENCHMARK_INDICES]
    macro_metrics = {
        "Macro_AUROC": np.nanmean(benchmark_aurocs),
        "All_Class_Macro_AUROC": np.nanmean(metrics["auroc"])
    }
    
    # Handle NaN case
    if np.isnan(macro_metrics["Macro_AUROC"]):
        macro_metrics["Macro_AUROC"] = 0.0
        
    return macro_metrics, metrics, y_probs, y_true

def optimize_thresholds(y_true, y_probs):
    """
    Find optimal per-class thresholds by maximizing Matthews Correlation Coefficient.


    """
    best_thresholds = []
    # 50-point grid search from 0 to 1 as per 
    search_space = np.linspace(0, 1, 50)
    
    for i in range(NUM_CLASSES):
        best_mcc, best_thr = -1.0, 0.5
        
        for thr in search_space:
            # Skip extreme edges to avoid zero-division in MCC
            if thr == 0 or thr == 1:
                continue
            
            # Apply threshold
            pred = (y_probs[:, i] > thr).astype(int)
            mcc = matthews_corrcoef(y_true[:, i], pred)
            
            if mcc > best_mcc:
                best_mcc = mcc
                best_thr = thr
                
        best_thresholds.append(best_thr)
        
    return np.array(best_thresholds)


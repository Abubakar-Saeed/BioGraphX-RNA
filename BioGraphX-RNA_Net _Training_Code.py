# 🔍 5. GATING ANALYSIS FUNCTION
# ==========================================
def analyze_gating(model, dataloader):
    """
    Analyze the gating mechanism to understand feature importance per compartment.
    
    Args:
        model: Trained BioGraphX_RNA model
        dataloader: DataLoader for analysis (typically test set)
    """
    model.eval()
    
    # Storage for all batches
    all_emb_acts = []
    all_phys_acts = []
    all_labels = []
    
    print("\n🔍 Scanning Test Set for Gate Activations...")
    
    with torch.no_grad():
        for emb, phys, labels in dataloader:
            emb, phys = emb.to(DEVICE), phys.to(DEVICE)
            
            # Forward pass components
            e_feat = model.embedding_branch(emb)
            p_feat = model.physics_branch(phys)
            combined = torch.cat([e_feat, p_feat], dim=1)
            
            # Get gate values
            gate_values = model.gate(combined)
            
            # Split gate into embedding and physics components
            split_idx = gate_values.shape[1] // 2
            g_emb = gate_values[:, :split_idx]
            g_phys = gate_values[:, split_idx:]
            
            # Store mean activation per sample
            all_emb_acts.append(g_emb.mean(dim=1).cpu().numpy())
            all_phys_acts.append(g_phys.mean(dim=1).cpu().numpy())
            all_labels.append(labels.numpy())
            
    # Concatenate all batches
    final_emb_acts = np.concatenate(all_emb_acts)   # Shape: (N_Samples,)
    final_phys_acts = np.concatenate(all_phys_acts) # Shape: (N_Samples,)
    final_labels = np.vstack(all_labels)            # Shape: (N_Samples, N_Classes)
    
    # 1. Global Analysis
    avg_emb = np.mean(final_emb_acts)
    avg_phys = np.mean(final_phys_acts)
    tot = avg_emb + avg_phys
    
    print("\n" + "="*60)
    print(f"🌍 GLOBAL GATING ANALYSIS (All {len(final_emb_acts)} samples)")
    print("="*60)
    print(f"Avg Embedding Gate: {avg_emb:.4f}  ({(avg_emb/tot)*100:.1f}%)")
    print(f"Avg Physics Gate:   {avg_phys:.4f}  ({(avg_phys/tot)*100:.1f}%)")
    print("-" * 60)
    
    # 2. Class-by-Class Analysis
    print(f"{'Compartment':<25} | {'N_Samples':<10} | {'Emb Gate':<10} | {'Phys Gate':<10} | {'Phys Reliance %':<15}")
    print("-" * 75)
    
    for i, col in enumerate(ALL_TARGET_COLS):
        # Find indices where this class is Positive (1)
        class_indices = np.where(final_labels[:, i] == 1)[0]
        n_samples = len(class_indices)
        
        if n_samples > 0:
            # Extract gate values for these specific samples
            c_emb = np.mean(final_emb_acts[class_indices])
            c_phys = np.mean(final_phys_acts[class_indices])
            
            c_tot = c_emb + c_phys
            phys_pct = (c_phys / c_tot) * 100
            
            print(f"{col:<25} | {n_samples:<10} | {c_emb:.4f}     | {c_phys:.4f}     | {phys_pct:.1f}%")
        else:
            print(f"{col:<25} | 0          | --         | --         | --")
            
    print("="*60)
    print("Interpretation: Higher 'Phys Reliance %' means the model uses")
    print("structural graph features more heavily for that compartment.")


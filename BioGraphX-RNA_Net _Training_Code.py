# 🚀 6. MAIN PIPELINE
# ==========================================
def main():
    print("🚀 STARTING OPTIMIZED 5-FOLD PIPELINE")
    print(f"🔧 Params: LR={LEARNING_RATE} | Batch={BATCH_SIZE} | Drop={DROPOUT_RATE:.2f} | Gamma={FOCAL_GAMMA:.2f}")
    
    # ===== 1. Load Data =====
    train_full_df = pd.read_csv(TRAIN_CSV_PATH)
    test_df = pd.read_csv(TEST_CSV_PATH)
    all_cols = list(train_full_df.columns)

    # BioGraphX-RNA physics features are the last 149 columns
    RNA_PHYSICS_COLS = all_cols[-149:]
    
    # Ensure target columns exist
    for col in ALL_TARGET_COLS:
        if col not in train_full_df.columns:
            print(f"❌ Missing target column: {col}")
            return
        if col not in test_df.columns:
            test_df[col] = 0  # Add dummy column if missing

    # Filter to valid physics columns
    valid_physics_cols = [c for c in RNA_PHYSICS_COLS if c in train_full_df.columns]
    
    # ===== 2. Preprocessing =====
    # Standardize physics features
    scaler = StandardScaler()
    X_train_phys = scaler.fit_transform(train_full_df[valid_physics_cols]).astype(np.float32)
    X_test_phys = scaler.transform(test_df[valid_physics_cols]).astype(np.float32)
    
    # Extract labels
    Y_train_full = train_full_df[ALL_TARGET_COLS].values.astype(np.float32)
    Y_test = test_df[ALL_TARGET_COLS].values.astype(np.float32)
    
    # Load embeddings
    embedding_dict = load_embeddings(EMBEDDINGS_PATH)
    
    # ===== 3. K-Fold Setup =====
    kfold = KFold(n_splits=N_FOLDS, shuffle=True, random_state=42)
    test_ensemble_probs = np.zeros((len(test_df), NUM_CLASSES))
    all_fold_thresholds = []  #store thresholds per validation fold
    fold_results = []
    
    for fold, (train_idx, val_idx) in enumerate(kfold.split(train_full_df)):
        print(f"\n{'='*20} FOLD {fold+1}/{N_FOLDS} {'='*20}")
        
        # Create datasets
        train_ds = RNAHybridDataset(
            train_full_df.iloc[train_idx], 
            embedding_dict, 
            X_train_phys[train_idx], 
            Y_train_full[train_idx]
        )
        val_ds = RNAHybridDataset(
            train_full_df.iloc[val_idx], 
            embedding_dict, 
            X_train_phys[val_idx], 
            Y_train_full[val_idx]
        )
        
        # Create dataloaders
        train_dl = DataLoader(
            train_ds, 
            batch_size=BATCH_SIZE, 
            shuffle=True, 
            collate_fn=collate_fn_rna, 
            num_workers=NUM_WORKERS
        )
        val_dl = DataLoader(
            val_ds, 
            batch_size=BATCH_SIZE, 
            shuffle=False, 
            collate_fn=collate_fn_rna, 
            num_workers=NUM_WORKERS
        )
        
        # Initialize model
        model = BioGraphX_RNA(
            EMBEDDING_DIM, 
            len(valid_physics_cols), 
            NUM_CLASSES, 
            SHARED_DIM, 
            DROPOUT_RATE
        ).to(DEVICE)
        
        # Loss, optimizer, scheduler
        criterion = FocalLoss(alpha=0.25, gamma=FOCAL_GAMMA)
        optimizer = torch.optim.AdamW(
            model.parameters(), 
            lr=LEARNING_RATE, 
            weight_decay=1e-3
        )
        scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(
            optimizer, 
            mode='max', 
            factor=0.5, 
            patience=5
        )
        
        # Training loop variables
        best_metric = -1.0
        best_model_state = None
        patience_counter = 0
        
        # ===== 4. Training Loop =====
        for epoch in range(N_EPOCHS):
            model.train()
            train_loss = 0
            
            for emb, phys, labels in train_dl:
                emb, phys, labels = emb.to(DEVICE), phys.to(DEVICE), labels.to(DEVICE)
                
                optimizer.zero_grad()
                loss = criterion(model(emb, phys), labels)
                loss.backward()
                
                # Gradient clipping for stability
                torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
                optimizer.step()
                
                train_loss += loss.item()
            
            # Validation
            macro, _, _, _ = evaluate_metrics(model, val_dl)
            curr_metric = macro["Macro_AUROC"]
            scheduler.step(curr_metric)
            
            # Save best model
            if curr_metric > best_metric:
                best_metric = curr_metric
                best_model_state = model.state_dict().copy()
                patience_counter = 0
            else:
                patience_counter += 1
            
            # Progress update
            print(f"Epoch {epoch+1}: Loss {train_loss/len(train_dl):.4f} | Benchmark AUROC {curr_metric:.4f} | Best {best_metric:.4f}", end='\r')
            
            # Early stopping
            if patience_counter >= PATIENCE:
                break

        # ===== 5. Post-Fold Optimization =====
        model.load_state_dict(best_model_state)
        torch.save(model.state_dict(), f"best_model_fold_{fold+1}.pth")
        fold_results.append(best_metric)

        # --- Validation-based Threshold Optimization ---
        print(f"\n🔍 Optimizing Thresholds on Validation Fold {fold+1}...")
        _, _, val_probs, val_true = evaluate_metrics(model, val_dl)
        fold_opt_thrs = optimize_thresholds(val_true, val_probs)
        print(f"Optimal thresholds: {fold_opt_thrs}")
        all_fold_thresholds.append(fold_opt_thrs)
        
        # Add to Test Ensemble
        test_ds_fold = RNAHybridDataset(test_df, embedding_dict, X_test_phys, Y_test)
        test_dl_fold = DataLoader(
            test_ds_fold, 
            batch_size=BATCH_SIZE, 
            shuffle=False, 
            collate_fn=collate_fn_rna
        )
        _, _, test_logits, _ = evaluate_metrics(model, test_dl_fold)
        test_ensemble_probs += test_logits / N_FOLDS
        
        # Cleanup
        del model
        torch.cuda.empty_cache()
        gc.collect()

    # ===== 6. Final Global Calculations =====
    print(f"\nAverage Benchmark AUROC Across Folds: {np.nanmean(fold_results):.4f}")
    
    # Calculate Final Ensemble Thresholds (Mean of best thresholds per fold)
    final_vld_thresholds = np.mean(all_fold_thresholds, axis=0)
    
    print("\n" + "="*45)
    print(" FINAL ENSEMBLED THRESHOLDS")
    print("-" * 45)
    for i, col in enumerate(ALL_TARGET_COLS):
        print(f"{col:<20}: {final_vld_thresholds[i]:.4f}")
    print("="*45)

    # Apply averaged validation thresholds to test ensemble probabilities
    test_preds_opt = np.zeros_like(test_ensemble_probs)
    for i in range(NUM_CLASSES):
        test_preds_opt[:, i] = (test_ensemble_probs[:, i] > final_vld_thresholds[i]).astype(int)

    # ===== 7. Final Metric Reports (Using optimized predictions) =====
    print(f"\n{'='*40} TABLE 1 (BENCHMARK) {'='*40}")
    print(f"{'Compartment':<20} | {'F1':<8} | {'MCC':<8} | {'AUROC':<8} | {'AUPRC':<8}")
    print("-" * 75)
    
    final_metrics = {"f1": [], "mcc": [], "auroc": [], "auprc": []}
    for i in BENCHMARK_INDICES:
        col = ALL_TARGET_COLS[i]
        
        # Calculate metrics
        try:
            auroc = roc_auc_score(Y_test[:, i], test_ensemble_probs[:, i])
        except:
            auroc = 0.5
            
        try:
            auprc = average_precision_score(Y_test[:, i], test_ensemble_probs[:, i])
        except:
            auprc = 0.0
        
        mcc = matthews_corrcoef(Y_test[:, i], test_preds_opt[:, i])
        f1 = f1_score(Y_test[:, i], test_preds_opt[:, i], zero_division=0)
        

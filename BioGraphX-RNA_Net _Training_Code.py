class FocalLoss(nn.Module):
    """
    Focal Loss for multi-label classification with class imbalance.
    
    Focal loss down-weights easy examples and focuses training on hard negatives.
    Particularly useful for RNA localization where some compartments are rare.
    
    Formula: FL(p_t) = -α(1-p_t)^γ log(p_t)
    where p_t is model's estimated probability for the true class
    
    Args:
        alpha (float): Balancing factor (default: 0.25)
        gamma (float): Focusing parameter (default: 2.0)
        reduction (str): 'mean', 'sum', or 'none'
    """
    def __init__(self, alpha=0.25, gamma=2, reduction='mean'):
        super(FocalLoss, self).__init__()
        self.alpha = alpha
        self.gamma = gamma
        self.reduction = reduction

    def forward(self, inputs, targets):
        """
        Args:
            inputs: Raw logits from model (before sigmoid)
            targets: Binary labels (0 or 1)
        """
        BCE_loss = F.binary_cross_entropy_with_logits(inputs, targets, reduction='none')
        pt = torch.exp(-BCE_loss)  # p_t = sigmoid(logit) when target=1, else 1-sigmoid(logit)
        F_loss = self.alpha * (1-pt)**self.gamma * BCE_loss

        if self.reduction == 'mean': 
            return torch.mean(F_loss)
        elif self.reduction == 'sum': 
            return torch.sum(F_loss)
        else: 
            return F_loss


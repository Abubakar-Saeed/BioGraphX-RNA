# 3. OPTIMIZED MODEL ARCHITECTURE
# ==========================================
class BioGraphX_RNA(nn.Module):
    """
    Hybrid architecture combining RNA language model embeddings with graph-physics features.
    
    """
    def __init__(self, embedding_dim, physics_dim, num_classes, shared_dim, dropout_rate):
        super().__init__()
        
        # Branch 1: Language Model Embeddings (RiNALMo)
        # Captures evolutionary and sequence pattern information
        self.embedding_branch = nn.Sequential(
            nn.Linear(embedding_dim, shared_dim),
            nn.BatchNorm1d(shared_dim),  # Stabilizes training
            nn.ReLU(),
            nn.Dropout(dropout_rate)     # Prevents overfitting
        )
        
        # Branch 2: Graph Physics Features (BioGraphX-RNA)
        # Encodes structural constraints and interaction networks
        # Deeper architecture to capture complex interaction patterns
        self.physics_branch = nn.Sequential(
            nn.Linear(physics_dim, shared_dim),
            nn.BatchNorm1d(shared_dim),
            nn.ReLU(),
            nn.Dropout(dropout_rate)      # Tuned dropout rate
        )
        
        # Gating Mechanism: Learns to weight branches per sample
        # Output in [0,1] range via sigmoid activation
        self.gate = nn.Sequential(
            nn.Linear(shared_dim * 2, shared_dim * 2),
            nn.Sigmoid()  # Gate values between 0 and 1
        )
        
        # Classifier: Multi-label prediction head
        self.classifier = nn.Sequential(
            nn.Linear(shared_dim * 2, shared_dim // 2),
            nn.BatchNorm1d(shared_dim // 2),
            nn.ReLU(),
            nn.Dropout(dropout_rate),
            nn.Linear(shared_dim // 2, num_classes)  # Raw logits output
        )
        
    def forward(self, embedding, physics):
        """
        Forward pass through the hybrid architecture.
        
        Args:
            embedding: RiNALMo embeddings (batch_size, 1280)
            physics: BioGraphX-RNA features (batch_size, 149)
            
        Returns:
            Logits for multi-label classification (batch_size, 9)
        """
        # Process each branch independently
        e_feat = self.embedding_branch(embedding)  # (batch, 512)
        p_feat = self.physics_branch(physics)      # (batch, 512)
        
        # Combine features
        combined = torch.cat([e_feat, p_feat], dim=1)  # (batch, 1024)
        
        # Apply gating: element-wise multiplication with learned weights
        # This allows the model to emphasize relevant features per sample
        gated = combined * self.gate(combined)  # (batch, 1024)
        
        # Final classification
        return self.classifier(gated)  # (batch, 9)

def load_embeddings(embeddings_path):
    """
    Load RiNALMo RNA language model embeddings from .npz file.
    
    The embeddings file contains pre-computed representations for each RNA sequence,
    generated by the RiNALMo model (1280-dimensional vectors).
    
    Args:
        embeddings_path: Path to .npz file
        
    Returns:
        Dictionary mapping Gene_ID to embedding vector, or None if file missing
    """
    print(f"Loading embeddings from {embeddings_path}...")
    if not os.path.exists(embeddings_path):
        print("Embedding file not found.")
        return None
    
    try:
        embeddings = np.load(embeddings_path, allow_pickle=True)
        embedding_dict = {}
        
        # Handle different possible NPZ structures
        for key in embeddings.files:
            if key == 'arr_0' or key == '0':
                data = embeddings[key]
                if hasattr(data, 'dtype') and data.dtype.names:
                    # Structured array format
                    for name in data.dtype.names:
                        embedding_dict[str(name)] = data[name]
                elif isinstance(data, dict):
                    # Dictionary format
                    embedding_dict = {str(k): v for k, v in data.items()}
            else:
                # Direct key-value format
                embedding_dict[str(key)] = embeddings[key]
                
        print(f"✅ Loaded {len(embedding_dict)} embeddings.")
        return embedding_dict
        
    except Exception as e:
        print(f"❌ Error loading embeddings: {e}")
        return None

class RNAHybridDataset(Dataset):
    """
    PyTorch Dataset for RNA sequences combining embeddings and physics features.
    
    Each sample contains:
    - Gene_ID: Identifier for looking up pre-computed embeddings
    - Physics features: 149-dimensional BioGraphX-RNA features
    - Labels: Multi-label targets for 9 compartments
    
    If embedding is missing, returns zero vector (model will rely on physics branch).
    """
    def __init__(self, df, embedding_dict, physics_array, label_matrix):
        self.gene_ids = df["Gene_ID"].values.astype(str)
        self.labels = torch.tensor(label_matrix, dtype=torch.float32)
        self.embedding_dict = embedding_dict
        self.physics = torch.tensor(physics_array, dtype=torch.float32)

    def __len__(self):
        return len(self.gene_ids)
    
    def __getitem__(self, idx):
        gene_id = self.gene_ids[idx]
        
        # Get embedding if available
        if self.embedding_dict and gene_id in self.embedding_dict:
            emb = self.embedding_dict[gene_id]
            # Handle variable-length embeddings (average pooling)
            if len(emb.shape) > 1:
                emb = np.mean(emb, axis=0)
            embedding_tensor = torch.from_numpy(emb).float()
        else:
            # Missing embedding: use zeros (model will rely on physics)
            embedding_tensor = torch.zeros(EMBEDDING_DIM, dtype=torch.float32)
            
        return embedding_tensor, self.physics[idx], self.labels[idx]

def collate_fn_rna(batch):
    """
    Custom collate function for RNAHybridDataset.
    
    Args:
        batch: List of (embedding, physics, labels) tuples
        
    Returns:
        Stacked embeddings, physics features, and labels
    """
    e, p, l = zip(*batch)
    return torch.stack(e), torch.stack(p), torch.stack(l)


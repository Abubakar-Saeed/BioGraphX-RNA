    "Frustration_GC_Variance",  # Variance of frustration at GC nucleotides
    "Frustration_AU_Mean",  # Average frustration at AU nucleotides
    "Frustration_AU_Variance",  # Variance of frustration at AU nucleotides

    # Structural contrasts (2)
    "Frustration_5vs3_Prime",  # Frustration difference: 5' minus 3' region
    "Frustration_StructuredVsFlexible",  # Frustration difference: GC minus AU


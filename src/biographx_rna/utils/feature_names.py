    "Frustration_Paired_Mean",  # Average frustration at paired nucleotides

    # Hotspot analysis (2)
    "Frustration_HotspotCount",  # Number of high-frustration positions
    "Frustration_HotspotDensity",  # Density of frustration hotspots

    # Motif-associated frustration (3)
    "Frustration_Motif_Mean",  # Average frustration in motif regions
    "Frustration_Motif_Max",  # Maximum frustration in motif regions
    "Frustration_NonMotif_Mean",  # Average frustration in non-motif regions


# =============================================================================
# 1. BASIC RNA GRAPH FEATURES (65 dimensions)
# =============================================================================
"""
Basic graph features capture the topology and interaction patterns of the RNA
constraint graph. These features quantify network properties derived from
RNA-specific interactions (canonical pairing, wobble pairs, stacking, backbone).
"""
RNA_BASIC_GRAPH_NAMES = [
    # Basic topology (3)
    # Fundamental graph properties describing overall network structure
    "Node_Count",  # Number of nucleotides (nodes) in the graph
    "Edge_Count",  # Number of interactions (edges) detected
    "Graph_Density",  # Edge density: actual_edges / possible_edges

    # Degree statistics (6)
    # Distribution of node connectivity (number of interactions per nucleotide)
    "Mean_Degree",  # Average number of interactions per nucleotide
    "Degree_Std",  # Standard deviation of degree distribution
    "Max_Degree",  # Maximum degree (most connected nucleotide)
    "Degree_Q1",  # First quartile of degree distribution
    "Degree_Median",  # Median degree
    "Degree_Q3",  # Third quartile of degree distribution

    # Weighted degree (3)
    # Degree measures weighted by interaction strength
    "Mean_Weighted_Degree",  # Average interaction strength per nucleotide
    "Weighted_Degree_Std",  # Standard deviation of weighted degree
    "Max_Weighted_Degree",  # Maximum weighted degree (strongest connected)

    # RNA interaction type ratios (5)
    # Relative frequency of each interaction type in the graph
    "Canonical_WC_Ratio",  # Proportion of Watson-Crick pairs (A-U, G-C)
    "Wobble_Pair_Ratio",  # Proportion of G-U wobble pairs
    "Non_Canonical_Ratio",  # Proportion of other non-canonical pairs
    "Base_Stacking_Ratio",  # Proportion of stacking interactions
    "Backbone_Ratio",  # Proportion of backbone connections

    # Energy-based features (5)
    # Thermodynamic properties derived from interaction energies
    "Mean_Interaction_Energy",  # Average energy per interaction (kcal/mol)
    "Energy_Std",  # Standard deviation of interaction energies
    "Min_Energy",  # Minimum (most negative) energy - strongest interaction
    "Total_Stabilization",  # Sum of all interaction energies
    "Strong_Interactions_Ratio",  # Proportion of interactions with energy < -1.0 kcal/mol

    # Centrality features (10)
    # Measures of node importance in the network (with cutoff=10 for efficiency)
    "Mean_Betweenness",  # Average betweenness centrality (nodes on shortest paths)
    "Betweenness_Std",  # Standard deviation of betweenness
    "Max_Betweenness",  # Maximum betweenness (structural bottlenecks)
    "Mean_Closeness",  # Average closeness centrality (nodes near others)
    "Closeness_Std",  # Standard deviation of closeness
    "Max_Closeness",  # Maximum closeness (centrally located nodes)
    "Min_Closeness",  # Minimum closeness (peripheral nodes)
    "Mean_Eigenvector",  # Average eigenvector centrality (influence)
    "Eigenvector_Std",  # Standard deviation of eigenvector centrality

    # Community features (6)
    # Detection of structural domains/modules in RNA
    "Community_Count",  # Number of structural domains detected
    "Modularity",  # Quality of community division (0-1)
    "Mean_Community_Size",  # Average size of structural domains
    "Community_Size_Std",  # Standard deviation of domain sizes
    "Max_Community_Size",  # Size of largest structural domain
    "IntraCommunity_Edge_Ratio",  # Proportion of edges within communities (domain cohesion)

    # RNA localization patterns (12)
    # Region-specific graph properties in functionally important areas
    "Five_Prime_Connectivity",  # Edge density in 5' region (first 30 nt)
    "Five_Prime_GC",  # GC content in 5' region (structural potential)
    "Five_Prime_Strong_Edges",  # Strong interaction ratio in 5' region
    "Three_Prime_Connectivity",  # Edge density in 3' region (last 30 nt)
    "Three_Prime_AU",  # AU content in 3' region (flexibility signal)
    "Three_Prime_Terminal_Degree",  # Connectivity of 3' terminal nucleotide
    "GC_Cluster_Connectivity",  # Edge density within GC-rich regions
    "GC_Cluster_Count",  # Number of GC-rich clusters
    "Max_GC_Cluster_Size",  # Size of largest GC-rich cluster
    "AU_Cluster_Connectivity",  # Edge density within AU-rich regions
    "AU_Cluster_Count",  # Number of AU-rich clusters
    "Max_AU_Cluster_Size",  # Size of largest AU-rich cluster

    # Path features (6)
    # Communication paths and efficiency measures
    "Avg_Shortest_Path",  # Average shortest path length between nucleotides
    "Graph_Diameter",  # Maximum shortest path (graph diameter)
    "Global_Efficiency",  # 1/avg_path - information transfer efficiency
    "Local_Efficiency",  # Average efficiency of local neighborhoods
    "Path_Length_Std",  # Standard deviation of path lengths
    "Max_Path_Length",  # Maximum path length observed

    # Additional RNA metrics (10)
    # Supplementary graph properties specific to RNA
    "Clustering_Coefficient",  # Local connectivity density (triangles)
    "Assortativity",  # Degree correlation (preferential attachment)
    "Strong_Interaction_Ratio",  # Overall ratio of strong interactions
    "Mean_Energy",  # Mean interaction energy (repeated for clarity)
    "Min_Energy_Overall",  # Minimum energy overall (most stable)
    "Stabilization_Per_Nucleotide",  # Total energy per nucleotide
    "Hybrid_Edge_Ratio",  # Overall ratio of hybrid edges
    "Mean_Edge_Weight",  # Average edge weight (interaction strength)
    "Edge_Weight_Std",  # Standard deviation of edge weights
    "Max_Edge_Weight"  # Maximum edge weight (strongest interaction)
]

# =============================================================================

# =============================================================================
"""
Hybrid features capture combined interactions where multiple interaction types
occur between the same nucleotides, indicating enhanced structural stability
and potential functional significance.
"""
RNA_HYBRID_FEATURE_NAMES = [
    # Basic hybrid scores (3)
    # Normalized scores for each hybrid interaction type
    "WC_Stacking_Hybrid",  # Watson-Crick + Stacking hybrid score
    "Wobble_Stacking_Hybrid",  # Wobble + Stacking hybrid score
    "Stacking_Backbone_Hybrid",  # Stacking + Backbone hybrid score

    # Regional hybrid density (4)
    # Concentration of hybrid interactions in specific regions
    "Five_Prime_Hybrid_Density",  # Hybrid edge density in 5' region
    "Three_Prime_Hybrid_Density",  # Hybrid edge density in 3' region
    "GC_Region_Hybrid_Density",  # Hybrid edge density in GC-rich regions
    "AU_Region_Hybrid_Density",  # Hybrid edge density in AU-rich regions

    # Hybrid network properties (3)
    # Graph properties of the hybrid interaction subnetwork
    "Hybrid_Edge_Ratio_Detailed",  # Detailed ratio of hybrid edges
    "Hybrid_Subgraph_Density",  # Density of hybrid-only interactions
    "Hybrid_Cluster_Count",  # Number of hybrid interaction clusters

    # Hybrid energy features (3)
    # Energetic properties of hybrid interactions
    "Mean_Hybrid_Energy",  # Average energy of hybrid interactions
    "Min_Hybrid_Energy",  # Minimum (most stable) hybrid energy
    "Strong_Hybrid_Ratio",  # Ratio of strong hybrid interactions

    # Advanced hybrid metrics (2)
    # Higher-order hybrid interaction properties
    "Hybrid_Diversity",  # Shannon entropy of hybrid type distribution
    "Hybrid_Connectivity"  # Average degree in hybrid subnetwork
]

# =============================================================================
# 3. RNA PROFILE FEATURE NAMES (27 dimensions)
# =============================================================================
"""
Profile features combine three types of evidence for each of nine subcellular
compartments: motif-based scores, hybrid interaction scores, and GC content
compatibility. These features are derived from RNAMotifProfiler.
"""
RNA_PROFILE_FEATURE_NAMES = []
for loc in ["Nucleus", "Exosome", "Cytosol", "Cytoplasm", "Ribosome",
            "Membrane", "Endoplasmic.reticulum", "Microvesicles", "Mitochondrion"]:
    RNA_PROFILE_FEATURE_NAMES.extend([
        f"Profile_{loc}_Motif",  # Motif-based localization score
        f"Profile_{loc}_Hybrid",  # Hybrid interaction compatibility score
        f"Profile_{loc}_GC_Compat"  # GC content compatibility with compartment
    ])

